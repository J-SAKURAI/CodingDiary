package com.huihuan.gmp.vo;

public class WrapperVo {
	private String id;
	

	private String code;
	private String name;
	private String quantityUnit1;
	private String specialManageType;
	private String status;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getQuantityUnit1() {
		return quantityUnit1;
	}
	public void setQuantityUnit1(String quantityUnit1) {
		this.quantityUnit1 = quantityUnit1;
	}
	public String getSpecialManageType() {
		return specialManageType;
	}
	public void setSpecialManageType(String specialManageType) {
		this.specialManageType = specialManageType;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

}
